def v2(n):
    """2-adic valuation of n."""
    if n == 0:
        return float('inf')
    v = 0
    while n % 2 == 0:
        n //= 2
        v += 1
    return v


def odd_part(n):
    """Odd part of n."""
    while n > 0 and n % 2 == 0:
        n //= 2
    return n


def T_collatz(n):
    """Compressed Collatz map on odd numbers: T(n) = odd(3n+1)."""
    return odd_part(3 * n + 1)


def compute_A_sequence(k, x):
    """
    Compute the A-sequence: A_0 = 3k+1, A_{m+1} = 3*A_m + 2^{v_2(A_m)}.
    Returns (As, Ds) where Ds[m] = v_2(A_m).
    """
    A = 3 * k + 1
    As = [A]
    Ds = [v2(A)]
    for m in range(1, x):
        A = 3 * A + 2 ** Ds[-1]
        As.append(A)
        Ds.append(v2(A))
    return As, Ds


def compute_g_classical(positions, x):
    """Classical g(v) = sum_{j=0}^{x-1} 3^{x-1-j} * 2^{positions[j]}."""
    return sum(3 ** (x - 1 - j) * 2 ** positions[j] for j in range(x))


def is_aperiodic(positions, S):
    """Check if the binary vector with 1s at 'positions' is aperiodic in {0,...,S-1}."""
    pos_set = set(positions)
    for p in range(1, S):
        if S % p != 0:
            continue
        if all((q + p) % S in pos_set for q in pos_set):
            return False
    return True


