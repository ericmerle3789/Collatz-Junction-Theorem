def S_of_k(k):
    """S(k) = ceil(k * log2(3))."""
    return math.ceil(k * math.log2(3))


def d_of_k(k):
    """d(k) = 2^S - 3^k."""
    S = S_of_k(k)
    return (1 << S) - 3**k


def prime_factors(n):
    """Return sorted list of distinct prime factors of |n|."""
    n = abs(n)
    if n <= 1:
        return []
    factors = []
    d = 2
    while d * d <= n:
        if n % d == 0:
            factors.append(d)
            while n % d == 0:
                n //= d
        d += 1
    if n > 1:
        factors.append(n)
    return sorted(factors)


def multiplicative_order(base, p):
    """Multiplicative order of base mod p. Returns 0 if gcd(base,p) != 1."""
    if math.gcd(base, p) != 1:
        return 0
    r = 1
    for m in range(1, p):
        r = (r * base) % p
        if r == 1:
            return m
    return p - 1


def modinv(a, p):
    """Modular inverse of a mod p (p prime)."""
    return pow(a, p - 2, p)


def is_prime(n):
    """Primality test."""
    if n < 2:
        return False
    if n < 4:
        return True
    if n % 2 == 0 or n % 3 == 0:
        return False
    i = 5
    while i * i <= n:
        if n % i == 0 or n % (i + 2) == 0:
            return False
        i += 6
    return True


