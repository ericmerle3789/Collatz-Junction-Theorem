def analyze_geometric_structure(k_range):
    """For each prime p | d(k), analyze the cyclic subgroup <2> mod p.

    Computes:
      - ord_p(2) : multiplicative order of 2 mod p
      - Coverage : ord_p(2) / (p-1), fraction of (Z/pZ)* covered
      - Whether 2 is a primitive root mod p
    """
    print("=" * 72)
    print("  S2. GEOMETRIC STRUCTURE OF <2> MOD p")
    print("=" * 72)
    print()
    print("  For each k in {7,...,20} and each prime p | d(k):")
    print("    ord_p(2) = multiplicative order of 2 in (Z/pZ)*")
    print("    coverage = ord_p(2) / (p-1)")
    print("    prim.root = whether 2 generates all of (Z/pZ)*")
    print()

    results = {}
    all_primes_seen = set()
    primitive_root_count = 0
    total_prime_count = 0

    hdr = (f"  {'k':>3} {'S':>4} {'d(k)':>20} {'p':>14} {'ord_p(2)':>10} "
           f"{'coverage':>10} {'prim':>5}")
    print(hdr)
    print("  " + "-" * (len(hdr) - 2))

    for k in k_range:
        S = S_of_k(k)
        d = d_of_k(k)
        if d <= 1:
            continue
        primes = prime_factors(d)

        for p in primes:
            if p < 3:
                continue
            all_primes_seen.add(p)
            total_prime_count += 1

            ord_2 = multiplicative_order(2, p)
            coverage = ord_2 / (p - 1) if p > 1 else 0
            prim = (ord_2 == p - 1)
            if prim:
                primitive_root_count += 1

            results[(k, p)] = {
                'S': S, 'd': d, 'ord_2': ord_2,
                'coverage': coverage, 'primitive_root': prim
            }

            prim_str = "YES" if prim else "no"
            print(f"  {k:>3} {S:>4} {d:>20} {p:>14} {ord_2:>10} "
                  f"{coverage:>10.4f} {prim_str:>5}")

    print()
    print(f"  Summary: {len(all_primes_seen)} distinct primes, k = {min(k_range)}..{max(k_range)}")
    print(f"    Primitive root count: {primitive_root_count}/{total_prime_count} "
          f"({100*primitive_root_count/max(total_prime_count,1):.1f}%)")

    coverages = [r['coverage'] for r in results.values()]
    if coverages:
        print(f"    Coverage: min={min(coverages):.4f}, max={max(coverages):.4f}, "
              f"mean={sum(coverages)/len(coverages):.4f}")
        full_cov = sum(1 for c in coverages if abs(c - 1.0) < 1e-10)
        print(f"    Full coverage (prim. root): {full_cov}/{len(coverages)}")

    print()
    return results


