def analyze_consecutive_ratios(k_range_detail):
    """Analyze the consecutive ratios in the Horner chain.

    Consecutive multipliers: 2^{b_{k-1-j}} then 2^{b_{k-2-j}}.
    Ratio = 2^{b_{k-2-j}} / 2^{b_{k-1-j}} = 2^{-(gap)} mod p, gap >= 1.

    KEY QUESTION: Does the constraint that all ratios are negative powers of 2
    (with gap >= 1) reduce randomness compared to unconstrained sampling?
    """
    print("=" * 72)
    print("  S4. CONSECUTIVE RATIO ANALYSIS")
    print("=" * 72)
    print()
    print("  In the Horner chain (decreasing order), consecutive multiplier")
    print("  ratios are 2^{-gap} mod p where gap = b_{i+1} - b_i >= 1.")
    print("  We analyze how constrained these ratios are.")
    print()

    for k in k_range_detail:
        S = S_of_k(k)
        d = d_of_k(k)
        if d <= 1:
            continue

        n_subsets = math.comb(S - 1, k - 1)
        primes = prime_factors(d)

        for p in primes:
            if p < 3 or n_subsets > 100000:
                continue

            ord_2 = multiplicative_order(2, p)

            # Possible ratio values: 2^{-g} for g = 1, ..., S-2
            possible_ratios = set()
            for g in range(1, S):
                possible_ratios.add(pow(modinv(2, p), g, p))
            coverage_ratio = len(possible_ratios) / (p - 1)

            # Collect gap distributions for corrSum=0 vs !=0
            gap_counter_zero = Counter()
            gap_counter_nonzero = Counter()
            n_zero = 0
            n_total = 0

            for subset in combinations(range(1, S), k - 1):
                n_total += 1
                c = horner_chain_ordered(subset, k, p)

                # Inner gaps
                inner_gaps = [subset[i+1] - subset[i] for i in range(len(subset)-1)]

                if c == 0:
                    n_zero += 1
                    for g in inner_gaps:
                        gap_counter_zero[g] += 1
                else:
                    for g in inner_gaps:
                        gap_counter_nonzero[g] += 1

            print(f"  k={k}, p={p}, ord_p(2)={ord_2}, S={S}")
            print(f"    Possible ratios (gaps 1..{S-1}): {len(possible_ratios)}/{p-1} "
                  f"of (Z/pZ)* ({coverage_ratio:.2%})")
            print(f"    Solutions: {n_zero}/{n_total} "
                  f"(P(0)={n_zero/n_total:.6f}, 1/p={1/p:.6f})")

            # Compare gap distributions
            total_g0 = sum(gap_counter_zero.values())
            total_gnz = sum(gap_counter_nonzero.values())
            if total_g0 > 0 and total_gnz > 0:
                # Mean gap
                mean_g0 = sum(g * c for g, c in gap_counter_zero.items()) / total_g0
                mean_gnz = sum(g * c for g, c in gap_counter_nonzero.items()) / total_gnz
                print(f"    Mean gap (sol):   {mean_g0:.4f}")
                print(f"    Mean gap (non):   {mean_gnz:.4f}")
                print(f"    Gap delta:        {mean_g0 - mean_gnz:+.4f}")

                # Show distribution
                all_gaps_set = sorted(set(gap_counter_zero.keys()) |
                                      set(gap_counter_nonzero.keys()))
                print(f"    {'gap':>6} {'freq(sol)':>10} {'freq(non)':>10} {'ratio':>8}")
                for g in all_gaps_set[:8]:
                    f0 = gap_counter_zero.get(g, 0) / total_g0
                    fnz = gap_counter_nonzero.get(g, 0) / total_gnz
                    r = f0 / fnz if fnz > 0 else float('inf')
                    r_s = f"{r:.4f}" if r != float('inf') else "inf"
                    print(f"    {g:>6} {f0:>10.4f} {fnz:>10.4f} {r_s:>8}")

            print()

    return


