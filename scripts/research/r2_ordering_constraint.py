def analyze_subgroup_structure(k_range):
    """Analyze <2, 3> mod p for each prime p | d(k).

    When ord_p(2) < p-1, the values 2^b lie in a proper subgroup H.
    But corrSum involves BOTH 2^{b_j} AND 3^{j} coefficients.
    The relevant algebraic structure is the subgroup <2, 3> of (Z/pZ)*.
    If <2, 3> = (Z/pZ)*, corrSum can reach all residues mod p.
    """
    print("=" * 72)
    print("  S5. SUBGROUP STRUCTURE: <2, 3> MOD p")
    print("=" * 72)
    print()
    print("  corrSum = sum 3^{j-1} * 2^{b_j}. The terms involve both powers")
    print("  of 2 (from exponents) and powers of 3 (from Horner coefficients).")
    print("  If <2,3> is a proper subgroup of (Z/pZ)*, corrSum is restricted")
    print("  to cosets of this subgroup, potentially affecting P(corrSum=0).")
    print()

    hdr = (f"  {'k':>3} {'p':>12} {'ord_p(2)':>10} {'ord_p(3)':>10} "
           f"{'|<2,3>|':>10} {'p-1':>10} {'full?':>6} {'index':>6}")
    print(hdr)
    print("  " + "-" * (len(hdr) - 2))

    subgroup_data = []

    for k in k_range:
        S = S_of_k(k)
        d = d_of_k(k)
        if d <= 1:
            continue

        for p in prime_factors(d):
            if p < 3:
                continue

            ord_2 = multiplicative_order(2, p)
            ord_3 = multiplicative_order(3, p)

            # In the cyclic group (Z/pZ)*, the subgroup generated by 2 and 3
            # has order lcm(ord_p(2), ord_p(3)). This follows from the fact
            # that every subgroup of a cyclic group is cyclic, and the
            # subgroup generated by subgroups of orders d1 and d2 has
            # order lcm(d1, d2).
            size_23 = math.lcm(ord_2, ord_3)
            is_full = (size_23 == p - 1)
            index = (p - 1) // size_23 if size_23 > 0 else 0

            subgroup_data.append({
                'k': k, 'p': p, 'ord_2': ord_2, 'ord_3': ord_3,
                'size_23': size_23, 'is_full': is_full, 'index': index
            })

            full_str = "YES" if is_full else "NO"
            print(f"  {k:>3} {p:>12} {ord_2:>10} {ord_3:>10} "
                  f"{size_23:>10} {p-1:>10} {full_str:>6} {index:>6}")

    # Summary
    if subgroup_data:
        full_count = sum(1 for s in subgroup_data if s['is_full'])
        total = len(subgroup_data)
        print()
        print(f"  Summary: <2,3> = (Z/pZ)* for {full_count}/{total} primes "
              f"({100*full_count/max(total,1):.1f}%)")
        if full_count == total:
            print("  RESULT: corrSum can reach ALL residues mod p for EVERY prime.")
            print("  The subgroup structure does NOT restrict the range of corrSum.")
        else:
            non_full = [s for s in subgroup_data if not s['is_full']]
            print(f"  WARNING: {len(non_full)} primes have <2,3> as proper subgroup:")
            for s in non_full:
                print(f"    k={s['k']}, p={s['p']}: |<2,3>|={s['size_23']}, "
                      f"index={(s['p']-1)//s['size_23']}")
                # For these, P(corrSum=0) could differ from 1/p
                # If 0 is not in the image of corrSum, then P=0 instead of 1/p!
                # But 0 is the identity element and is always reachable (empty sum).
                # The question is whether corrSum can equal 0 when it's a sum
                # of terms from a proper coset.

    print()
    return subgroup_data


