def compare_ordered_vs_shuffled(k_range_detail, n_shuffles=50):
    """Compare P(corrSum=0) for ordered vs randomly shuffled processing.

    For each subset, the corrSum depends on the ORDER in which elements
    are processed. The standard order is decreasing; shuffled is random.

    If ordering matters, P_ordered != P_shuffled.
    """
    print("=" * 72)
    print("  S6. ORDERED vs SHUFFLED PROCESSING")
    print("=" * 72)
    print()
    print("  For each subset, we compare corrSum with:")
    print("  (a) Standard decreasing order (ORDERED)")
    print(f"  (b) {n_shuffles} random permutations (SHUFFLED)")
    print("  (c) Markov prediction (1/p)")
    print()
    print("  If ordering creates bias, P_ordered != P_shuffled.")
    print()

    hdr = (f"  {'k':>3} {'p':>6} {'C':>8} "
           f"{'P_ord':>10} {'P_shuf':>10} {'1/p':>10} "
           f"{'ord/1p':>8} {'shuf/1p':>8} {'ord/shuf':>9}")
    print(hdr)
    print("  " + "-" * (len(hdr) - 2))

    results = []

    for k in k_range_detail:
        S = S_of_k(k)
        d = d_of_k(k)
        if d <= 1:
            continue

        n_subsets = math.comb(S - 1, k - 1)
        if n_subsets > 100000:
            continue

        for p in prime_factors(d):
            if p < 3:
                continue

            N_ordered_zero = 0
            N_shuffled_zero = 0
            N_shuffled_total = 0
            N_total = 0

            for subset in combinations(range(1, S), k - 1):
                N_total += 1

                # Ordered
                c_ord = horner_chain_ordered(subset, k, p)
                if c_ord == 0:
                    N_ordered_zero += 1

                # Shuffled
                elements = list(subset)
                for _ in range(n_shuffles):
                    perm = elements[:]
                    RNG.shuffle(perm)
                    c_shuf = horner_chain_arbitrary_order(perm, p)
                    N_shuffled_total += 1
                    if c_shuf == 0:
                        N_shuffled_zero += 1

            P_ord = N_ordered_zero / N_total if N_total > 0 else 0
            P_shuf = N_shuffled_zero / N_shuffled_total if N_shuffled_total > 0 else 0
            inv_p = 1.0 / p

            r_ord = P_ord / inv_p
            r_shuf = P_shuf / inv_p
            r_os = P_ord / P_shuf if P_shuf > 0 else float('inf')

            results.append({
                'k': k, 'p': p, 'N_total': N_total,
                'P_ordered': P_ord, 'P_shuffled': P_shuf,
                'ratio_ord_unif': r_ord, 'ratio_shuf_unif': r_shuf,
                'ratio_ord_shuf': r_os,
            })

            print(f"  {k:>3} {p:>6} {N_total:>8} "
                  f"{P_ord:>10.6f} {P_shuf:>10.6f} {inv_p:>10.6f} "
                  f"{r_ord:>8.4f} {r_shuf:>8.4f} {r_os:>9.4f}")

    # Summary
    if results:
        print()
        r_ords = [r['ratio_ord_unif'] for r in results]
        r_shufs = [r['ratio_shuf_unif'] for r in results]
        r_oss = [r['ratio_ord_shuf'] for r in results
                 if r['ratio_ord_shuf'] != float('inf')]
        print(f"  Ordered/uniform : mean={sum(r_ords)/len(r_ords):.4f}, "
              f"range=[{min(r_ords):.4f}, {max(r_ords):.4f}]")
        print(f"  Shuffled/uniform: mean={sum(r_shufs)/len(r_shufs):.4f}, "
              f"range=[{min(r_shufs):.4f}, {max(r_shufs):.4f}]")
        if r_oss:
            print(f"  Ordered/shuffled: mean={sum(r_oss)/len(r_oss):.4f}, "
                  f"range=[{min(r_oss):.4f}, {max(r_oss):.4f}]")
        print()
        mean_os = sum(r_oss) / len(r_oss) if r_oss else 0
        if abs(mean_os - 1.0) < 0.05:
            print("  RESULT: P(corrSum=0) is ROBUST to reordering.")
            print("  The ordering constraint has NO measurable effect.")
        elif mean_os < 1.0:
            print("  RESULT: Ordered chain produces FEWER zeros (aids exclusion).")
        else:
            print("  RESULT: Ordered chain produces MORE zeros (hinders exclusion).")

    print()
    return results


