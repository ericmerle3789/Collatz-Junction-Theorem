def horner_chain_ordered(subset_sorted, k, p):
    """Compute Horner chain in the STANDARD decreasing order.

    Elements are processed in order b_{k-1}, b_{k-2}, ..., b_1 (decreasing).
    Transition: c -> 3*c + 2^a mod p.

    Parameters:
        subset_sorted: tuple/list of (k-1) elements sorted ASCENDING
        k: cycle length
        p: prime modulus

    Returns:
        c_{k-1}: final value mod p (= corrSum mod p)
    """
    km1 = k - 1
    c = 0
    for j in range(km1):
        a = subset_sorted[km1 - 1 - j]
        c = (3 * c + pow(2, a, p)) % p
    return c


def horner_chain_full(subset_sorted, k, p):
    """Compute FULL Horner chain c_0, ..., c_{k-1} mod p (for detailed analysis).

    Returns list [c_0, c_1, ..., c_{k-1}].
    """
    km1 = k - 1
    chain = [0]
    for j in range(km1):
        a = subset_sorted[km1 - 1 - j]
        c_next = (3 * chain[-1] + pow(2, a, p)) % p
        chain.append(c_next)
    return chain


def horner_chain_arbitrary_order(elements_in_order, p):
    """Compute Horner chain with elements processed in given order.

    Returns c_final mod p.
    """
    c = 0
    for a in elements_in_order:
        c = (3 * c + pow(2, a, p)) % p
    return c


def corrsum_direct(subset_sorted, k, p):
    """Compute corrSum directly: sum_{i=1}^{k-1} 3^{i-1} * 2^{b_i} mod p.

    This matches the backward Horner chain:
      c_{k-1} = sum_{i=1}^{k-1} 3^{i-1} * 2^{b_i} mod p
    """
    km1 = k - 1
    s = 0
    for i in range(km1):
        s = (s + pow(3, i, p) * pow(2, subset_sorted[i], p)) % p
    return s


