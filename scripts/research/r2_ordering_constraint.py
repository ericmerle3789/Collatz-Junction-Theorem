def compute_fingerprint(bias_results, subgroup_data, shuffle_results, allperm_results):
    """Compute SHA256 fingerprint of key numerical results."""
    data = []

    if bias_results:
        for r in sorted(bias_results, key=lambda x: (x['k'], x['p'])):
            data.append(('bias', r['k'], r['p'], r['sol_count'], r['N'],
                         round(r['P_zero'], 12), r['method']))

    if subgroup_data:
        for s in sorted(subgroup_data, key=lambda x: (x['k'], x['p'])):
            data.append(('subgroup', s['k'], s['p'], s['ord_2'], s['ord_3'],
                         s['size_23'], s['is_full']))

    if shuffle_results:
        for r in sorted(shuffle_results, key=lambda x: (x['k'], x['p'])):
            data.append(('shuffle', r['k'], r['p'],
                         round(r['P_ordered'], 12),
                         round(r['P_shuffled'], 12)))

    if allperm_results:
        for a in sorted(allperm_results, key=lambda x: (x['k'], x['p'])):
            data.append(('allperm', a['k'], a['p'],
                         a['N_ordered_zero'], a['rank'],
                         round(a['mean_perm'], 8),
                         round(a['percentile'], 4)))

    sig = json.dumps(data, sort_keys=True)
    return hashlib.sha256(sig.encode()).hexdigest()


