# S3. ORDERING BIAS: P(corrSum=0) vs 1/p
# ============================================================================

def analyze_ordering_bias(k_range_exact, k_range_sample, n_samples=200000):
    """Compare P(corrSum=0) to 1/p for each (k, p) pair.

    Exact enumeration for small k, random sampling for large k.

    Returns list of result dicts.
    """
    print("=" * 72)
    print("  S3. ORDERING BIAS: P(corrSum=0) vs 1/p")
    print("=" * 72)
    print()
    print("  P(corrSum=0) should equal 1/p if the ordering has no effect.")
    print("  We compute the exact ratio P(corrSum=0) / (1/p) for k = 7..20.")
    print("  (* = exact enumeration, others = sampling with 200K samples)")
    print()

    rng = random.Random(RNG_SEED)

    results = []

    hdr = (f"  {'k':>3} {'p':>12} {'ord(2)':>8} {'N':>10} {'#sol':>10} "
           f"{'P(0)':>12} {'1/p':>12} {'ratio':>8} {'95% CI':>22} {'':>2}")
    print(hdr)
    print("  " + "-" * (len(hdr) - 2))

    all_k = sorted(set(k_range_exact) | set(k_range_sample))

    for k in all_k:
        S = S_of_k(k)
        d = d_of_k(k)
        if d <= 1:
            continue

        n_total = math.comb(S - 1, k - 1)
        primes = prime_factors(d)

        for p in primes:
            if p < 3:
                continue

            ord_2 = multiplicative_order(2, p)
            use_exact = (k in k_range_exact) and (n_total <= 500000)

            if use_exact:
                sol_count = 0
                for subset in combinations(range(1, S), k - 1):
                    if horner_chain_ordered(subset, k, p) == 0:
                        sol_count += 1
                N = n_total
                P_zero = sol_count / N
                ci_low, ci_high = P_zero, P_zero
                tag = "*"
            else:
                sol_count = 0
                pool = list(range(1, S))
                N = n_samples
                for _ in range(N):
                    subset = sorted(rng.sample(pool, k - 1))
                    if horner_chain_ordered(subset, k, p) == 0:
                        sol_count += 1
                P_zero = sol_count / N
                # Wilson confidence interval
                z = 1.96
                phat = P_zero
                denom = 1 + z**2 / N
                center = (phat + z**2 / (2*N)) / denom
                margin = z * math.sqrt(phat*(1-phat)/N + z**2/(4*N**2)) / denom
                ci_low = max(0, center - margin)
                ci_high = min(1, center + margin)
                tag = " "

            inv_p = 1.0 / p
            ratio = P_zero / inv_p if inv_p > 0 else float('inf')

            results.append({
                'k': k, 'p': p, 'ord_2': ord_2,
                'N': N, 'sol_count': sol_count,
                'P_zero': P_zero, 'inv_p': inv_p, 'ratio': ratio,
                'ci_low': ci_low, 'ci_high': ci_high,
                'method': 'exact' if tag == '*' else 'sample',
            })

            ci_str = (f"[{ci_low:.6f},{ci_high:.6f}]"
                      if tag != '*' else f"{'(exact)':>22}")
            print(f"  {k:>3} {p:>12} {ord_2:>8} {N:>10} {sol_count:>10} "
                  f"{P_zero:>12.6f} {inv_p:>12.6f} {ratio:>8.4f} "
                  f"{ci_str:>22} {tag:>2}")

    # Summary
    if results:
        ratios = [r['ratio'] for r in results]
        print()
        print("  Summary of P(corrSum=0) / (1/p):")
        print(f"    Mean ratio  : {sum(ratios)/len(ratios):.6f}")
        stdev = (sum((r - sum(ratios)/len(ratios))**2 for r in ratios) / len(ratios))**0.5
        print(f"    Std ratio   : {stdev:.6f}")
        print(f"    Range       : [{min(ratios):.6f}, {max(ratios):.6f}]")

        # t-test: is mean ratio significantly different from 1?
        if len(ratios) > 1:
            mean_r = sum(ratios) / len(ratios)
            se = stdev / len(ratios)**0.5
            t_stat = (mean_r - 1.0) / se if se > 0 else 0
            print(f"    t-statistic : {t_stat:.3f} (H0: ratio = 1)")
            sig = abs(t_stat) > 2.0
            print(f"    Significant : {'YES' if sig else 'NO'} at 95% level")

        # Check: is 1/p within CI for sampled cases?
        sampled = [r for r in results if r['method'] == 'sample']
        if sampled:
            in_ci = sum(1 for r in sampled if r['ci_low'] <= r['inv_p'] <= r['ci_high'])
            print(f"    1/p in 95% CI: {in_ci}/{len(sampled)} sampled cases")

        neg_count = sum(1 for r in ratios if r < 1.0)
        print(f"    Ratio < 1 (fewer solutions than 1/p): {neg_count}/{len(ratios)}")

        # Filtered summary: exclude cases where expected count N/p < 5
        # (standard rule for reliable chi-squared / ratio statistics)
        well_sampled = [r for r in results if r['N'] * r['inv_p'] >= 5]
        if well_sampled and len(well_sampled) < len(results):
            ws_ratios = [r['ratio'] for r in well_sampled]
            ws_mean = sum(ws_ratios) / len(ws_ratios)
            ws_std = (sum((r - ws_mean)**2 for r in ws_ratios) / len(ws_ratios))**0.5
            print()
            print(f"  Filtered summary (N/p >= 5, {len(well_sampled)}/{len(results)} cases):")
            print(f"    Mean ratio  : {ws_mean:.6f}")
            print(f"    Std ratio   : {ws_std:.6f}")
            print(f"    Range       : [{min(ws_ratios):.6f}, {max(ws_ratios):.6f}]")
            if len(ws_ratios) > 1:
                ws_se = ws_std / len(ws_ratios)**0.5
                ws_t = (ws_mean - 1.0) / ws_se if ws_se > 0 else 0
                print(f"    t-statistic : {ws_t:.3f}")
                print(f"    Significant : {'YES' if abs(ws_t) > 2.0 else 'NO'} at 95% level")
            print(f"    NOTE: Excludes {len(results)-len(well_sampled)} cases where")
            print(f"    expected count N/p < 5 (unreliable for ratio estimation)")

    print()
    return results


