def exhaustive_all_orders(k_range_tiny):
    """For very small k, enumerate ALL (k-1)! orderings of each subset.

    Gives the EXACT rank of the standard ordering among all permutations.
    """
    print("=" * 72)
    print("  S7. EXHAUSTIVE ALL-PERMUTATIONS COMPARISON")
    print("=" * 72)
    print()
    print("  For small k, we enumerate ALL (k-1)! orderings of each subset")
    print("  and rank the standard (decreasing) ordering by solution count.")
    print()

    hdr = (f"  {'k':>3} {'p':>6} {'(k-1)!':>6} {'C':>8} "
           f"{'N0_ord':>6} {'mean':>8} {'std':>8} "
           f"{'rank':>6} {'%ile':>6}")
    print(hdr)
    print("  " + "-" * (len(hdr) - 2))

    allperm_results = []

    for k in k_range_tiny:
        S = S_of_k(k)
        d = d_of_k(k)
        if d <= 1:
            continue

        km1 = k - 1
        n_perms = math.factorial(km1)
        n_subsets = math.comb(S - 1, km1)

        # Skip if too expensive
        if n_perms * n_subsets > 10000000:
            continue

        for p in prime_factors(d):
            if p < 3:
                continue

            all_perms = list(permutations(range(km1)))

            N_total = 0
            N_ordered_zero = 0
            perm_zero_counts = Counter()

            for subset in combinations(range(1, S), km1):
                N_total += 1

                c_ord = horner_chain_ordered(subset, k, p)
                if c_ord == 0:
                    N_ordered_zero += 1

                elements = list(subset)
                for perm_idx, perm in enumerate(all_perms):
                    reordered = [elements[perm[i]] for i in range(km1)]
                    c = horner_chain_arbitrary_order(reordered, p)
                    if c == 0:
                        perm_zero_counts[perm_idx] += 1

            # Which permutation index is "decreasing"?
            ordered_perm = tuple(range(km1 - 1, -1, -1))
            ordered_perm_idx = all_perms.index(ordered_perm)
            N_ord_from_perms = perm_zero_counts.get(ordered_perm_idx, 0)
            assert N_ord_from_perms == N_ordered_zero

            perm_counts = [perm_zero_counts.get(i, 0) for i in range(len(all_perms))]
            mean_perm = sum(perm_counts) / len(perm_counts)
            var_perm = sum((c - mean_perm)**2 for c in perm_counts) / len(perm_counts)
            std_perm = var_perm ** 0.5

            sorted_counts = sorted(perm_counts)
            rank = sorted_counts.index(N_ordered_zero) + 1
            percentile = rank / len(all_perms) * 100

            allperm_results.append({
                'k': k, 'p': p, 'n_perms': n_perms, 'N_total': N_total,
                'N_ordered_zero': N_ordered_zero,
                'mean_perm': mean_perm, 'std_perm': std_perm,
                'rank': rank, 'percentile': percentile,
            })

            print(f"  {k:>3} {p:>6} {n_perms:>6} {N_total:>8} "
                  f"{N_ordered_zero:>6} {mean_perm:>8.2f} {std_perm:>8.2f} "
                  f"{rank:>6} {percentile:>6.1f}")

    # Summary
    if allperm_results:
        pcts = [a['percentile'] for a in allperm_results]
        print()
        print(f"  Ordered percentile: mean={sum(pcts)/len(pcts):.1f}%, "
              f"min={min(pcts):.1f}%, max={max(pcts):.1f}%")
        avg = sum(pcts) / len(pcts)
        if 35 <= avg <= 65:
            print("  => Standard ordering is NEAR MEDIAN: no systematic bias.")
        elif avg < 35:
            print("  => Standard ordering produces FEWER zeros (aids exclusion).")
        else:
            print("  => Standard ordering produces MORE zeros (hinders exclusion).")

    print()
    return allperm_results


