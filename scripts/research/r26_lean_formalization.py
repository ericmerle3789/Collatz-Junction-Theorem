def assess_native_decide(k, blocking_prime):
    """
    Assess whether native_decide can handle the proof for given k.
    The decision problem has search space C(k) = C(S-1, k-1) vectors,
    each requiring a mod-p computation.
    native_decide works when the problem fits in ~2^30 operations.
    """
    C = compute_C(k)
    S = compute_S(k)
    max_B = S - k

    # Operations per vector: k multiplications and additions mod p
    ops_per_vec = 2 * k
    total_ops = C * ops_per_vec

    log2_ops = log(total_ops) / log(2) if total_ops > 0 else 0

    # native_decide feasible if log2_ops < 30 (rough threshold)
    feasible = log2_ops < 30
    # decide feasible if log2_ops < 20 (more conservative)
    decide_feasible = log2_ops < 20

    return {
        'k': k, 'blocking_prime': blocking_prime,
        'C': C, 'total_ops': total_ops, 'log2_ops': log2_ops,
        'native_decide_feasible': feasible,
        'decide_feasible': decide_feasible,
        'recommendation': (
            'decide' if decide_feasible else
            'native_decide' if feasible else
            'external_certificate'
        )
    }


