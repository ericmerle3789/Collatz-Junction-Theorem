def formalization_priorities(coverage):
    """Order formalization tasks by difficulty and impact."""
    priorities = []

    for k in sorted(coverage.keys()):
        status = coverage[k]['status']
        if status in ('COMPUTATIONAL', 'COMPUTATIONAL-NEW'):
            bp = coverage[k].get('blocking_prime')
            if bp:
                nd = assess_native_decide(k, bp)
                priorities.append({
                    'k': k,
                    'priority': 1 if nd['decide_feasible'] else 2 if nd['native_decide_feasible'] else 3,
                    'method': nd['recommendation'],
                    'log2_ops': nd['log2_ops'],
                    'blocking_prime': bp
                })
        elif status == 'OPEN':
            priorities.append({
                'k': k,
                'priority': 4,
                'method': 'verify_first',
                'log2_ops': None,
                'blocking_prime': None
            })

    priorities.sort(key=lambda x: (x['priority'], x.get('log2_ops', float('inf'))))
    return priorities


