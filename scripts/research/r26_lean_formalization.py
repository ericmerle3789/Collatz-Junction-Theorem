def generate_lean_stub_computational(k, blocking_prime):
    """Generate Lean 4 stub for a computationally verified k."""
    d = compute_d(k)
    S = compute_S(k)
    C = compute_C(k)
    g = compute_g(k, blocking_prime)

    stub = f"""
-- ============================================================
-- Theorem: no_cycle_k{k}
-- Generated by r26_lean_formalization.py (R26-D)
-- Status: COMPUTATIONAL (blocking prime p = {blocking_prime})
-- ============================================================

/-- For k = {k}, the equation n₀ · d(k) = corrSum(A) has no solution
    with n₀ ≥ 1 and A a valid k-step Collatz sequence.
    d({k}) = 2^{S} - 3^{k} = {d}.
    Blocking prime: p = {blocking_prime} | d({k}).
    N₀(p) = 0 verified by exhaustive DP.
    C({k}) = C({S-1}, {k-1}) = {C}. -/
theorem no_cycle_k{k} :
    ∀ (A : Fin {k} → ℕ),
      nondecreasing_B A {S - k} →
      (∑ j, (2 * (3 : ℤ)⁻¹) ^ (j : ℕ) * 2 ^ (A j : ℤ)) % {blocking_prime} ≠ 0 := by
  -- Proof strategy: enumerate all C({S-1},{k-1}) = {C} nondecreasing B-vectors
  -- and verify P_B(g) ≢ 0 (mod {blocking_prime}) for each.
  -- g = 2·3⁻¹ mod {blocking_prime} = {g}.
  -- This is decidable by native_decide if {blocking_prime} is small enough.
  sorry -- TODO: native_decide or decide tactic
"""
    return stub


