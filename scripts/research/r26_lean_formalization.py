def build_coverage_table():
    """Build coverage table: which k are PROVED-LEAN, COMPUTATIONAL, or OPEN."""
    table = {}

    # k=3..15: PROVED-LEAN (in Basic.lean, HigherCases.lean)
    for k in range(3, 16):
        table[k] = {
            'status': 'PROVED-LEAN',
            'file': 'Basic.lean' if k <= 11 else 'HigherCases.lean',
            'method': 'native_decide / algebraic'
        }

    # k=16..20: COMPUTATIONAL (verified by DP, not yet in Lean)
    for k in range(16, 21):
        d = compute_d(k)
        facs = factorize(d)
        bp = None
        for p, e in facs:
            if is_prime(p) and p <= 50000:
                bp = p
                break
        table[k] = {
            'status': 'COMPUTATIONAL',
            'blocking_prime': bp,
            'method': 'DP' if bp else 'CRT-sieve'
        }

    # k=21..41: OPEN (gap)
    for k in range(21, 42):
        table[k] = {'status': 'OPEN', 'method': 'needed'}

    # k>=42: CONDITIONAL (Borel-Cantelli tail < 1)
    for k in range(42, 51):
        table[k] = {'status': 'CONDITIONAL-BC', 'method': 'Borel-Cantelli'}

    return table


def update_coverage(table, r26c_results):
    """Update coverage table with R26-C results."""
    updated = dict(table)
    for k, result in r26c_results.items():
        if result.get('verdict') == 'PROVED':
            bp = result.get('blocking_prime')
            updated[k] = {
                'status': 'COMPUTATIONAL-NEW',
                'blocking_prime': bp,
                'method': f'DP on p={bp}'
            }
    return updated


